let blockKey = 0;
const key = () => `k${++blockKey}`;

function heading(level, text) {
    return {
        _type: "block",
        _key: key(),
        style: level,
        markDefs: [],
        children: [{ _type: "span", _key: key(), text, marks: [] }],
    };
}

function paragraph(text) {
    return {
        _type: "block",
        _key: key(),
        style: "normal",
        markDefs: [],
        children: parseInlineMarks(text),
    };
}

function blockquote(text) {
    return {
        _type: "block",
        _key: key(),
        style: "blockquote",
        markDefs: [],
        children: [{ _type: "span", _key: key(), text, marks: [] }],
    };
}

/** Parse **bold** markers inside text into proper Portable Text spans */
function parseInlineMarks(text) {
    const spans = [];
    const regex = /\*\*(.+?)\*\*/g;
    let lastIndex = 0;
    let match;

    while ((match = regex.exec(text)) !== null) {
        // Text before the bold
        if (match.index > lastIndex) {
            spans.push({
                _type: "span",
                _key: key(),
                text: text.slice(lastIndex, match.index),
                marks: [],
            });
        }
        // Bold text
        spans.push({
            _type: "span",
            _key: key(),
            text: match[1],
            marks: ["strong"],
        });
        lastIndex = match.index + match[0].length;
    }

    // Remaining text
    if (lastIndex < text.length) {
        spans.push({
            _type: "span",
            _key: key(),
            text: text.slice(lastIndex),
            marks: [],
        });
    }

    if (spans.length === 0) {
        spans.push({ _type: "span", _key: key(), text, marks: [] });
    }

    return spans;
}

